package com.shopfloor.backend.database.objects;

/**
 * Enumeration representing the different roles a user can have.
 * @author David Todorov (https://github.com/david-todorov)
 */
public enum Role {
    /**
     * Role for users who can edit content.
     */
    EDITOR,

    /**
     * Role for users who can operate the system.
     */
    OPERATOR
}
